	</p>
	<ul class="list-disc list-inside pl-4">
		<li>Información del dispositivo (modelo, sistema operativo y versión).</li>
		<li>Logs de fallos y errores.</li>
		<li>Información sobre el estado de la aplicación en el momento del error.</li>
	</ul>
	<p>
		Estos datos no contienen información personal y se utilizan únicamente para diagnosticar y
		resolver problemas técnicos.
	</p>
