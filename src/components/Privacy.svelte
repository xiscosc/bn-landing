    recopilamos ninguno.
  </p>
  <h2 class="text-xl font-semibold">4. Seguridad</h2>
  <p>
    Tomamos medidas técnicas y organizativas razonables para proteger la
    información recopilada contra accesos no autorizados, pérdidas o
    alteraciones.
