  <h2 class="text-xl font-semibold">1. Datos Personales</h2>
  <p>
    No recopilamos, almacenamos ni procesamos ningún tipo de datos personales de
    los usuarios que utilicen nuestras aplicaciones. Esto incluye, pero no se
    limita a, nombres, direcciones de correo electrónico, números de teléfono,
    ubicaciones o cualquier otra información identificable.
  </p>
  <h2 class="text-xl font-semibold">2. Analíticas de Errores</h2>
  <p>
    Con el propósito de mejorar el rendimiento y la estabilidad de nuestras
    aplicaciones, utilizamos herramientas de analíticas que recopilan
    información técnica relacionada con errores y fallos en la aplicación. Estos
    datos son exclusivamente técnicos e incluyen:
  </p>
  <ul class="list-disc list-inside pl-4">
    <li>Información del dispositivo (modelo, sistema operativo y versión).</li>
    <li>Logs de fallos y errores.</li>
    <li>
      Información sobre el estado de la aplicación en el momento del error.
    </li>
  </ul>
  <p>
    Estos datos no contienen información personal y se utilizan únicamente para
    diagnosticar y resolver problemas técnicos.
  </p>
  <h2 class="text-xl font-semibold">3. Uso de Herramientas de Terceros</h2>
  <p>
    Podemos emplear servicios de terceros para recopilar y analizar las
    analíticas de errores. Estos servicios cumplen con estrictos estándares de
